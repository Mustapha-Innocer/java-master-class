public class Ex6 {
	public static void main(String[] args) {
		
		int firstNumber = 10;
		int secondNumber = 12;
		int thirdNumber = 11;
		if (firstNumber < secondNumber || firstNumber > thirdNumber) {
			System.out.println("True");
		} else {
			System.out.println("False");
		}
	}
}
